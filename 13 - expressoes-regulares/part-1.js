/*
global: pega todos as letras
insensitive: não diferencia maiuscula de miniscula
*/

/*
^i: pega apenas o i do inicio da string
i$: pega apenas o i do fim da string
a|i: pega a letra a / e
i+: pega todos os i em sequencia tipo iiiii
i.: pega o i e qualguer letra em seguida
\^i: o \ serve para anular a funcionalidade do caracter especial
(): serve para agrupar expressões
\w: pega todos as letras e numeros anulando os ,*´+/ tudo isso
\W: faz o contrario de \w
\d: pega todos os numeros
\D: faz o contrario de \d
\s: pega os espaços
\S: pega tudo que não é espaço
[a,b,c]: pega todos os a b c
[a-z]: pega tudo do a até o z
a{1,3}: pega do primeiro até o terceiro
*/